    private fun defaultWarmups(count: Int): List<WarmupSetTarget> {
        val base = listOf(
            WarmupSetTarget(targetReps = 10, suggestedLoadKg = null, label = "50% x 8-10"),
            WarmupSetTarget(targetReps = 6, suggestedLoadKg = null, label = "70% x 5-8"),
            WarmupSetTarget(targetReps = 3, suggestedLoadKg = null, label = "85% x 2-3"),
        )
        return base.take(count.coerceAtLeast(0).coerceAtMost(base.size))
    }

    private fun defaultWorkSets(
        count: Int,
        repLabel: String,
        earlyRpeLabel: String?,
        lastRpeLabel: String?,
        restLabel: String,
    ): List<WorkSetTarget> {
        val restSeconds = parseRest(restLabel)
        val targetReps = parseRepMidpoint(repLabel)
        return List(count) { index ->
            val rpeLabel = if (index == count - 1) lastRpeLabel ?: earlyRpeLabel else earlyRpeLabel ?: lastRpeLabel
            WorkSetTarget(
                targetReps = targetReps,
                targetRepLabel = repLabel,
                targetRpe = parseApproxRpe(rpeLabel),
                targetRpeLabel = rpeLabel,
                restSeconds = restSeconds,
                restLabel = restLabel,
            )
        }
    }

    private fun parseRest(label: String): Int? = when (label) {
        "1-2 min" -> 90
        "2-3 min" -> 150
        "3-5 min" -> 240
        else -> null
    }

    private fun parseRepMidpoint(label: String): Int {
        val parts = label.split("-").mapNotNull { it.trim().toIntOrNull() }
        return if (parts.size == 2) (parts[0] + parts[1]) / 2 else label.toIntOrNull() ?: 8
    }

    private fun parseApproxRpe(label: String?): Double? {
        if (label == null || label == "N/A") return null
        val clean = label.replace("~", "")
        val parts = clean.split("-").mapNotNull { it.trim().toDoubleOrNull() }
        return if (parts.size == 2) (parts[0] + parts[1]) / 2.0 else clean.toDoubleOrNull()
    }

